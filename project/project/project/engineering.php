<p>in collaboration with the internationally acclaimed universities. Faculty of Engineering prides itself for keeping up to date with the advancing technologies through research and development activities, staff training and networking with international communities. Further, the Faculty hopes to acquire, promote, develop and disseminate knowledge of engineering sciences in order to improve the quality of life by equipping future generations with the right attitudes and skills to grow into competent individuals who could be considered an asset to the nation. The primary aim of the Faculty is forming engineers of the highest quality who, with experience, should be able to hold responsible positions at the highest levels of the profession, possessing the wisdom to recognize their professional development.
</p>
